#include "../ft_printf.h"

void		reverse(wchar_t **str)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = (ft_strlen_null(*str, 0) - 1);
	if ((*str)[i] == '-')
		i++;
	while (j > i)
	{
		c = (*str)[j];
		(*str)[j] = (*str)[i];
		(*str)[i] = c;
		i++;
		j--;
	}
}
